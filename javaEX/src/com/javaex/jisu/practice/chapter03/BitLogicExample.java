package com.javaex.jisu.practice.chapter03;

public class BitLogicExample {	//	비트 논리 연산자

	public static void main(String[] args) {
		System.out.println("45 & 25 = " + (45 & 25));
		System.out.println("45 | 25 = " + (45 | 25));
		System.out.println("45 ^ 25 = " + (45 ^ 25));
		System.out.println("~45 = " + (~45));

	}

}
